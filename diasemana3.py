print("Qual dia da semana é hoje ?")
diasemana = input()
if diasemana == "sabado":
    print("Hoje é dia de feijoada e uma cerveja!")
else:
    if diasemana == "sexta":
        print("Bora tomar uma")
    else:
        if diasemana == "domingo":
            print("Hoje é dia de ir na missa")
        else:
            print("Hoje não é dia de gastação")
